#include <stdio.h>
/**
 * op_add - sums two numbers
 * @a: the first number to add
 * @b: the second number to add
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- gives the difference of two numbers
 * @a: the first number to subtract
 * @b: the second number to subtract
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two numbers
 * @a: the first number
 * @b: the second number
 * Return: the multiple
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two numbers
 * @a: the first number
 * @b: the second number
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gives the modulus of a number
 * @a: number whose modulus you are finding
 * @b: the diviser
 * Return: the modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
